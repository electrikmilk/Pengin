  <h2>If I block someone can they still see my public threads?</h2>
  <p>If you block someone, threads you create will not show up for them anywhere and they won't be able to interact (like, reply, repost) with any posts in the thread.</p>
  <p>However, anyone you block will still be able to like and reply to the original post in your public thread.</p>
