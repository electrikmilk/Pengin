  <h2>Why can't I remove photos I attached to my post when editing it?</h2>
  <p>When you edit a post, everyone who has access to the post can see any changes you made by viewing the posts edit history. This edit history exists only if you've edited the post.</p>
  <p>If we added the ability to remove photos from your post when editing, because the post has an edit history, everyone who viewed the edit history would see the photos you wanted removed from the post. If instead we developed it so the photos were removed, this would defeat the purpose of edit history; authenticity.</p>
  <p>If you would like the photo you attached to the post removed, simply delete the post.</p>
